# 9. Write a Program to Convert meter to Feet and Inches
# 1 meter=12 inches
# 1 meter=100 centimeters
# 1 meter= 3.28084 Feet

meter = float(input("enter the value of meter: "))
feet= meter*3.28084
print("conversion of meter to feet is",feet,"Feet.")
inches = meter*12
print("conversion of meter to inches is",inches,"inches.")
