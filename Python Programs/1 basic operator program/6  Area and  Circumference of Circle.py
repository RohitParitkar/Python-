# 2. Write a Program to take radius of circle input from keyboard and calculate Area and 
# Circumference of Circle
# • Area = 3.14 * radius * radius 
# • Circumference = 2 * 3.14 * radius 
radius = float(input("Enter the values of radius: "))
area_of_circle = 3.14 * radius * radius 
print("area of circle: ",area_of_circle)
circumference_of_circle = 2 * 3.14 * radius
print("circumference of circle is: ",circumference_of_circle)
